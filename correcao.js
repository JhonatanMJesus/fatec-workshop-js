function somar() {
    const y = 5 //Declaração de variável que não pode ter o seu valor alterado.
    let x = 0; //Declaração de variável que possa ter seu valor alterado.
    x + 10; //Indica uma soma padrão com a variável "x".
    let r = x + y; //Declaração da variável "r" para armazenar o valor do resultado da soma das variáveis X e Y.
    console.log("O resultado da soma é: " + r) //Mostra no console o valor armazenado na variável "r"
}